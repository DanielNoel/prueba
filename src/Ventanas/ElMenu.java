    public void CargarFormulario(int dto) {
        boolean b = false;
        JInternalFrame frame = null;
        switch (dto) {
            case 1: {
                for (int i = 0; i < jDesktopPane2.getAllFrames().length; i++) {
                    if (jDesktopPane2.getAllFrames()[i] instanceof PROFESSIONES ) {
                        b = true;
                        frame = jDesktopPane2.getAllFrames()[i];
                        break;
                    }
                }
                if (!b) {
                   PROFESSIONES ObjFrmVentas = new PROFESSIONES();
                    setInternalFrame(ObjFrmVentas, 200, 100);
                } else {
                    frame.toFront();
                }
                break;
            }
             case 2: {
                for (int i = 0; i < jDesktopPane2.getAllFrames().length; i++) {
                    if (jDesktopPane2.getAllFrames()[i] instanceof EMPLEADO ) {
                        b = true;
                        frame = jDesktopPane2.getAllFrames()[i];
                        break;
                    }
                }
                if (!b) {
                   EMPLEADO ObjFrmVentas = new EMPLEADO();
                    setInternalFrame(ObjFrmVentas, 200, 100);
                } else {
                    frame.toFront();
                }
                break;
            }
           
           

           
           
            
            
            
           
           
           
            
           
          
           
         

        }
    }

